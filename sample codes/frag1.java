/**
 * A simple {@link Fragment} subclass.
 */
public class frag1 extends Fragment {
    static interface Listener{//interface declaration
        void itemclicked(String id1);//any function to pass data from fragment to activity
    };
    private Listener listener;
    public frag1() {
        // Required empty public constructor
    }
    @Override
    public void onAttach(Context context){//Registering interface
        super.onAttach(context);
        this.listener = (Listener)context;
    }
    @RequiresApi(api = Build.VERSION_CODES.LOLLIPOP)
    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        View v=inflater.inflate(R.layout.fragment_frag1, container, false);
        /*always use this way to inflate instead of getView() because getView() is nullable

        */
            Button b1 = (Button) v.findViewById(R.id.button1);
            b1.setOnClickListener(new View.OnClickListener() {
                @Override
                public void onClick(View v) {
                  if(listener!=null){
                     String id1="next";
                      listener.itemclicked(id1);
                  }
                }
            });
            return v;//always need to return view object
    }
    @Override
  public void onActivityCreated(Bundle savedInstanceState){
        /* Onactivity gets called after activity on completion of creation of activity
        */
        Toolbar toolbar=(Toolbar)getView().findViewById(R.id.toolbar2);
       ((MainActivity)getActivity()).setSupportActionBar(toolbar);
        super.onActivityCreated(savedInstanceState);
   }
    @Override
    public void onCreate(Bundle savedInstanceState){
        setHasOptionsMenu(true);//This function call is must when defining menu and menu items in fragments
        super.onCreate(savedInstanceState);
    }
    @Override
    public void onCreateOptionsMenu(Menu menu, MenuInflater inflater){
        inflater.inflate(R.menu.menu_main,menu);
        super.onCreateOptionsMenu(menu, inflater);
    }
    @Override
    public boolean onOptionsItemSelected(MenuItem item){
        View v=getView();
        switch(item.getItemId()){
            case (R.id.item1):
                final Snackbar snackbar=Snackbar.make(v.findViewById(R.id.frag1),"Hello",Snackbar.LENGTH_SHORT);

            default:  return super.onOptionsItemSelected(item);
        }
    }
}

























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































